package cmpt276.group4;

public class Position{
    private int x_axis;
    private int y_axis;

    public Position(int x_axis,int y_axis){
        this.x_axis = x_axis;
        this.y_axis = y_axis;
    }

    public int getX_axis(){
        return x_axis;
    }

    public int getY_axis(){
        return y_axis;
    }
}
